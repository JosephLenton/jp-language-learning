
export const KATAKANA_TO_ROMANJI : Record<string, string> = {
}

export function romaniseJPWord( word : string ):string {
  let result = ''

  for ( let i = 0; i < word.length; i++ ) {
    const char = word.charAt(i)

    result += romaniseJPChar(char)
  }

  return result
}

export function romaniseJPChar( jpChar : string ):string {
  const romanisedChar = KATAKANA_TO_ROMANJI[ jpChar ]

  if ( romanisedChar === undefined ) {
    return jpChar
  }

  return romanisedChar
}

